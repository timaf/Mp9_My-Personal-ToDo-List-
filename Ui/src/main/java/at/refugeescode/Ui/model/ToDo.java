package at.refugeescode.Ui.model;

public class ToDo {

    private String task;
    private String done;

    public ToDo() {
    }

    public String getTask() {
        return task;
    }

    public void setTask(String task) {
        this.task = task;
    }

    public String getDone() {
        return done;
    }

    public void setDone(String done) {
        this.done = done;
    }
}

